public class pattern2 {
    public static void main(String[] args) {
        int row=1;
        int star=1;
        int n=5;
        while(row<=n){
            int i=1;
            while(i<=star){
                System.out.print("*");
                i++;}
            star++;
            row++;
            System.out.println();
            }

    }
    
}
