public class pattern{
    public static void main(String[] args) {
        int row=1;
        int n=5;
        int star=5;
        while(row<=n){
            int i=1;
            while(i<=star){
                System.out.print("*");
                 i++;

            }
            row++;
            System.out.println();



        }
        

    }

}

